package vo;

public class MemberLinkSecretkey {

	
	private String secreKey;
	private String indexerID;
	private String nhisTableName;
	private String statTableName;
	
	
	public String getSecreKey() {
		return secreKey;
	}
	public void setSecreKey(String secreKey) {
		this.secreKey = secreKey;
	}
	public String getIndexerID() {
		return indexerID;
	}
	public void setIndexerID(String indexerID) {
		this.indexerID = indexerID;
	}
	public String getNhisTableName() {
		return nhisTableName;
	}
	public void setNhisTableName(String nhisTableName) {
		this.nhisTableName = nhisTableName;
	}
	public String getStatTableName() {
		return statTableName;
	}
	public void setStatTableName(String statTableName) {
		this.statTableName = statTableName;
	}
	
	
	
}
